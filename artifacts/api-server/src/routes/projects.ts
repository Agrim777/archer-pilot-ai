// POST /projects/:id/export
router.post("/projects/:id/export", requireAuth, async (req, res) => {
  const r = req as AuthenticatedRequest;
  const id = parseId(req.params.id, res);
  if (id === null) return;
  const parsed = ExportProjectBody.safeParse(req.body);
  if (!parsed.success) {
    res.status(400).json({ error: parsed.error.message });
    return;
  }

  try {
    const [project] = await db
      .select()
      .from(projects)
      .where(and(eq(projects.id, id), eq(projects.userId, r.userId)));

    if (!project) {
      res.status(404).json({ error: "Project not found" });
      return;
    }

    const { format } = parsed.data;
    const content = project.content as Record<string, unknown> | null;

    let exportContent = "";
    let filename = `${project.name.replace(/\s+/g, "_")}`;
    let mimeType = "application/octet-stream";

    switch (format) {
      case "json":
        exportContent = JSON.stringify(content, null, 2);
        filename += ".json";
        mimeType = "application/json";
        break;
      case "archer-package": {
        const zipBuffer = await buildExperimentalArcherPackage(project.name, content);
        res.json({
          format,
          filename: `${filename}.archer`,
          content: zipBuffer.toString("base64"),
          mimeType: "application/zip",
          experimental: true,
          warning:
            "This is an experimental, unofficial reconstruction of Archer's package format. " +
            "It is NOT generated by Archer itself and may fail Archer's Package Manager import validation. " +
            "If import fails, use the Build Guide export to create the application manually instead.",
        });
        return;
      }
      case "markdown": {
        exportContent = buildApplicationBuilderGuide(project.name, content);
        filename += "_build_guide.md";
        mimeType = "text/markdown";
        break;
      }
      default:
        exportContent = JSON.stringify(content, null, 2);
        filename += ".json";
        mimeType = "application/json";
    }

    res.json({
      format,
      filename,
      content: Buffer.from(exportContent).toString("base64"),
      mimeType,
    });
  } catch (err) {
    req.log.error(err);
    res.status(500).json({ error: "Failed to export project" });
  }
});

export default router;
